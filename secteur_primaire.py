target_maturities = ['1Y', '2Y', '3Y', '4Y', '5Y', '6Y', '10Y', '20Y']
target_countries = [
    "Japan", "India", "China",
    "Australia",
    "Tunisia", "Cameroon", "Cote d'Ivoire", "South Africa",
    "France", "Italy", "U.K.",
    "U.S.", "Canada"
]

data = []
